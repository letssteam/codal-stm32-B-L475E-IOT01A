void initScreen(STM32SPI& spi, STM32Pin& cs, STM32Pin& dc)
{
    dc.setDigitalValue(0);
    cs.setDigitalValue(0);
    spi.beginTransaction();

    spi.writeTransaction(0xAE);  // Set Display OFF

    spi.writeTransaction(0x15);  // Set Column Address
    spi.writeTransaction(0x00);  // Start Column Address
    spi.writeTransaction(0x3F);  // End Column Address

    spi.writeTransaction(0x75);  // Set Row Address (segment)
    spi.writeTransaction(0x00);  // Satrt Row Address
    spi.writeTransaction(0x7F);  // End Row Address

    spi.writeTransaction(0x81);  // Contrast Level
    spi.writeTransaction(0xFF);

    spi.writeTransaction(0xA0);  // Set Re-map
    spi.writeTransaction(0x42);  // Default Setting

    spi.writeTransaction(0xA1);  // Set Display Start Line
    spi.writeTransaction(0x00);

    spi.writeTransaction(0xA2);  // Set Display Offset
    spi.writeTransaction(0x00);

    spi.writeTransaction(0xA4);  // Set Display Mode
    // spi.writeTransaction(0xA5);	//Set Display Mode
    // spi.writeTransaction(0xA6);	//Set Display Mode
    // spi.writeTransaction(0xA7);	//Set Display Mode

    spi.writeTransaction(0xA8);  // Select Multiplex Ratio
    spi.writeTransaction(0x7F);  // Default => 0x3F (1/64 Duty)	0x1F(1/32 Duty)

    spi.writeTransaction(0xAB);  // Set Function SelectionA
    spi.writeTransaction(0x01);

    spi.writeTransaction(0xB1);  // Set Phase Length
    spi.writeTransaction(0xf1);

    spi.writeTransaction(0xB3);  // Set Display Clock Divide Ratio/Oscillator Frequency
    spi.writeTransaction(0x00);

    spi.writeTransaction(0xBC);  // Set Prechange Voltage
    spi.writeTransaction(0x07);

    spi.writeTransaction(0xBE);  // Set VCOMH Voltage
    spi.writeTransaction(0x07);

    spi.writeTransaction(0xB6);  // Set Second Pre-charge period
    spi.writeTransaction(0x2F);

    spi.writeTransaction(0xD5);  // Set Function selection B
    spi.writeTransaction(0x62);

    spi.writeTransaction(0xAF);  // Set Display On

    spi.endTransaction();
    cs.setDigitalValue(1);
}

void showPic(STM32SPI& spi, STM32Pin& cs, STM32Pin& dc)
{
    cs.setDigitalValue(0);

    dc.setDigitalValue(0);
    spi.beginTransaction();

    spi.writeTransaction(0x15);  // Set Column Address
    spi.writeTransaction(0x00);  // Start Column Address
    spi.writeTransaction(0x3F);  // End Column Address

    spi.writeTransaction(0x75);  // Set Row Address (segment)
    spi.writeTransaction(0x00);  // Satrt Row Address
    spi.writeTransaction(0x7F);  // End Row Address

    spi.endTransaction();

    dc.setDigitalValue(1);
    spi.beginTransaction();

    for (int i = 0; i < 128; i++) {
        for (int j = 0; j < 128 / 2; j++) {
            spi.writeTransaction(pic[i][j]);
        }
    }

    spi.endTransaction();
    cs.setDigitalValue(1);
    dc.setDigitalValue(0);
}
